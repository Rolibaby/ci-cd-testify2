# Declare a list with any value and number of item/element
language = ['Python', 'Java', 'Banana', 'Ruby', 'C#', 'NodeJS', 'C++']
print("List:", language)  # Print the list in console

# Sort the list in ascending order
language.sort()
print("Sort-asc:", language)  # Print the list in console again

# sort the list in descending order
language.sort(reverse=True)
print('Sort-Desc:', language)
